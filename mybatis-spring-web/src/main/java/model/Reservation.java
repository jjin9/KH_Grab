package model;

public class Reservation {
	private String bkName;
	private String mEmail;
	private String cEmail;
	private String resDate;
	private Integer resInt;
	private String regDate;
	private String resCheck;
	private String resCf;
	
	public String getBkName() {
		return bkName;
	}
	public void setBkName(String bkName) {
		this.bkName = bkName;
	}
	public String getmEmail() {
		return mEmail;
	}
	public void setmEmail(String mEmail) {
		this.mEmail = mEmail;
	}
	public String getcEmail() {
		return cEmail;
	}
	public void setcEmail(String cEmail) {
		this.cEmail = cEmail;
	}
	public String getResDate() {
		return resDate;
	}
	public void setResDate(String resDate) {
		this.resDate = resDate;
	}
	public Integer getResInt() {
		return resInt;
	}
	public void setResInt(Integer resInt) {
		this.resInt = resInt;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getResCheck() {
		return resCheck;
	}
	public void setResCheck(String resCheck) {
		this.resCheck = resCheck;
	}
	public String getResCf() {
		return resCf;
	}
	public void setResCf(String resCf) {
		this.resCf = resCf;
	}

}
