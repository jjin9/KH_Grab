package model;

public class Bucketlist {
	private String bkName;
	private String cEmail;
	private String bkContent;
	private Integer bkMax;
	private String bkPurpose;
	private String bkDate;
	private String bkDue;
	private String bkSort;
	private String bkCheck;
	public String getBkName() {
		return bkName;
	}
	public void setBkName(String bkName) {
		this.bkName = bkName;
	}
	public String getcEmail() {
		return cEmail;
	}
	public void setcEmail(String cEmail) {
		this.cEmail = cEmail;
	}
	public String getBkContent() {
		return bkContent;
	}
	public void setBkContent(String bkContent) {
		this.bkContent = bkContent;
	}
	public Integer getBkMax() {
		return bkMax;
	}
	public void setBkMax(Integer bkMax) {
		this.bkMax = bkMax;
	}
	public String getBkPurpose() {
		return bkPurpose;
	}
	public void setBkPurpose(String bkPurpose) {
		this.bkPurpose = bkPurpose;
	}
	public String getBkDate() {
		return bkDate;
	}
	public void setBkDate(String bkDate) {
		this.bkDate = bkDate;
	}
	public String getBkDue() {
		return bkDue;
	}
	public void setBkDue(String bkDue) {
		this.bkDue = bkDue;
	}
	public String getBkSort() {
		return bkSort;
	}
	public void setBkSort(String bkSort) {
		this.bkSort = bkSort;
	}
	public String getBkCheck() {
		return bkCheck;
	}
	public void setBkCheck(String bkCheck) {
		this.bkCheck = bkCheck;
	}
	
	
}
